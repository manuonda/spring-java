package com.demo.oauth;

public class Calculator {

    public int integerDivsion(int dividend, int divisor) {
        return dividend / divisor;
    }

    public int multiplication(int valor1, int valor2 ) {
        return valor1 * valor2;
    }

    public int integerSubstraction(int valor1, int valor2) {
        return valor1-valor2;
    }
}
